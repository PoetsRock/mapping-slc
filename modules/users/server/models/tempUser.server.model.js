/**
 * TempUser Schema - for verifying new user accounts
 **/
const TempUserSchema = new Schema({
  createdOn: {
    type: Date,
    default: Date.now
  },
  firstName: {
    type: String,
    trim: true,
    default: '',
    validate: [validateLocalStrategyProperty, 'Please fill in your first name']
  },
  lastName: {
    type: String,
    default: '',
    validate: [validateLocalStrategyProperty, 'Please fill in your last name'],
    trim: true
  },
  email: {
    type: String,
    unique: true,
    required: 'This email already exists',
    validate: [validateLocalStrategyEmail, 'Please use a valid email address'],
    trim: true
  },
  authToken: {
    type: String,
    trim: true,
    unique: true,
    default: generateAuthToken(),
  },
  username: {
    type: String,
    trim: true
  },
  password: {
    type: String,
    default: ''
  },
  salt: {
    type: String
  },
  provider: {
    type: String,
    required: 'Provider is required'
  },
  providerData: {},
  additionalProvidersData: {},
  roles: {
    type: [{
      type: String,
      enum: ['user', 'verificationRequired', 'blocked', 'unregistered', 'registered', 'contributor', 'admin', 'superUser']
    }],
    default: ['verificationRequired'],
  },
  /* For reset password */
  resetPasswordToken: {
    type: String
  },
  resetPasswordExpires: {
    type: Date
  }
});

/**
 * Hook a pre save method to hash the password
 */
TempUserSchema.pre('save', function (next) {
  if (this.password && this.isModified('password')) {
    this.salt = crypto.randomBytes(16).toString('base64');
    this.password = this.hashPassword(this.password);
  }

  next();
});

/**
 * Hook a pre validate method to test the local password
 */
TempUserSchema.pre('validate', function (next) {
  // Pass a hash of settings to the `config` method to override defaults
  owasp.config({
    minLength: 8  //this overrides default val -- original set to 10
  });
  if (this.provider === 'local' && this.password && this.isModified('password')) {
    var result = owasp.test(this.password);
    if (result.errors.length) {
      var error = result.errors.join(' ');
      this.invalidate('password', error);
    }
  }

  next();
});

/**
 * Create instance method for hashing a password
 */
TempUserSchema.methods.hashPassword = function (password) {
  if (this.salt && password) {
    return crypto.pbkdf2Sync(password, new Buffer(this.salt, 'base64'), 10000, 64).toString('base64');
  } else {
    return password;
  }
};

/**
 * Create instance method for authenticating user
 */
TempUserSchema.methods.authenticate = function (password) {
  return this.password === this.hashPassword(password);
};


